int	ft_any(char **tab, int (*f)(char*))
{
	int	ok;
	int	i;

	ok = 0;
	i = -1;
	while (tab[++i])
		if (!(f(tab[i])))
			ok++;
	return (!(ok == (i - 1)));
}
